# Carriage return (\r)
print("123456\rABC")  # replaces 123

# Backspace (\b)
print("Helloo\b")

# Bell/alert
print("\a")

# Vertical tab
print("Hello\vWorld")

# Form feed
print("Hello\fWorld")

# Using sep and end in print
print("Python", "is", "fun", sep=" - ")
print("This is the first line.", end=" ")
